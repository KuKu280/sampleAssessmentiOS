import UIKit

extension UIColor {
    
    /// CDL color namespace. Mostly contains base colors.
    public enum Assessment {
        
        /// dark Purple color of hex `#A8ACC1`.
        public static let darkPurple = UIColor(named: "darkpurple")!
        
        /// dark color of hex `#121212`.
        public static let dark = UIColor(named: "dark")!
        
        /// primaryBlue color of hex `#6637F5`.
        public static let primaryPurple = UIColor(named: "primaryPurple")!
        
        /// dark gray color of hex `#353535`.
        public static let darkGray = UIColor(named: "mlc.darkgrayText")!
    
        /// dark gray color of hex `#585858`.
        public static let placeHolder = UIColor(named: "placeholder")
        
        /// gray color of hex `#737373`.
        public static let gray = UIColor(named: "gray")
        
    }
}
